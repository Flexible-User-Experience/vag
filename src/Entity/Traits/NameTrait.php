<?php

namespace App\Entity\Traits;

/**
 * Trait NameTrait
 */
trait NameTrait
{
    /**
     * @return string|null
     */
    public function getName(): ?string
    {
        return $this->name;
    }

    /**
     * @param string|null $name
     *
     * @return $this
     */
    public function setName(?string $name): self
    {
        $this->name = $name;

        return $this;
    }
}
